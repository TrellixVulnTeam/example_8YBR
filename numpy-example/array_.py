print(np.array([[1, 2], [3, 4]]))

print("<< np.array([[1, 2], [3, 4]]).shape >>")
print(np.array([[1, 2], [3, 4]]).shape)

print("<< np.array(np.mat('1 2; 3 4')) >>")
print(np.array(np.mat('1 2; 3 4')))

