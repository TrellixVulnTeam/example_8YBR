#!/usr/bin/python
# -*- coding: utf-8 -*-

from __future__ import print_function
from __future__ import division

print("<<percent>>")
print("%.2f%%" % (12.345))

print("<<length>>")
print("%*.2f" % (7, 12.345))

print("<<char>>")
print("%c" % (65))

print("<<exponential>>")
print("%e" % (12.345))

